import pandas as pd
import matplotlib.pyplot as plt
file_path = '/content/ipl_colab.csv'
dataset = pd.read_csv(file_path)
print("\nData Types of All Columns:")
print(dataset.dtypes)
print("\nDescriptive Statistics for Numerical Columns:")
print(dataset.describe())
team_total_runs = dataset.groupby('batting_team')['runs'].sum().reset_index()
team_total_runs['Probability'] = team_total_runs['runs'] / team_total_runs['runs'].sum()
team_total_runs = team_total_runs.sort_values(by='Probability', ascending=False)
teams = team_total_runs['batting_team']
probabilities = team_total_runs['Probability']
plt.figure(figsize=(12, 6))
plt.barh(teams, probabilities, color='skyblue')
plt.xlabel('Winning Probability')
plt.ylabel('Teams')
plt.title('IPL Team Winning Probability - Bar Graph')
plt.gca().invert_yaxis()
plt.tight_layout()
plt.show()
plt.figure(figsize=(8, 8))
plt.pie(probabilities, labels=teams, autopct='%1.1f%%', startangle=140, colors=plt.cm.tab10.colors)
plt.title('IPL Team Winning Probability - Pie Chart')
plt.tight_layout()
plt.show()
plt.figure(figsize=(10, 6))
plt.plot(teams, probabilities, marker='o', linestyle='-', color='blue')
plt.xlabel('Teams')
plt.ylabel('Winning Probability')
plt.title('IPL Team Winning Probability - Line Graph')
plt.xticks(rotation=45, ha='right')
plt.grid(visible=True, linestyle='--', alpha=0.7)
plt.tight_layout()
plt.show()
